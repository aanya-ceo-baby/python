import turtle as t
t.screensize(450,450)
t.bgcolor("white")
t.color("black","orange")
t.pensize(3)
t.speed(0)
t.penup()
t.begin_fill()
t.setheading(90)
t.goto(100,90)
t.pendown()
t.circle(100,180)
t.forward(200)
t.circle(40,180)
t.forward(50)
t.setheading(-90)
t.circle(20,160)
t.setheading(-90)
t.forward(50)
t.circle(40,180)
t.goto(100,90)
t.end_fill()
t.penup()
t.begin_fill()
t.goto(80,80)
t.color("black","lightblue")
t.setheading(180)
t.pendown()
t.forward(80)
t.circle(40,180)
t.forward(80)
t.circle(40,180)
t.end_fill()
t.penup()

t.goto(-100,70)
t.color("black","blue")
t.pendown()
t.begin_fill()
t.forward(40)
t.left(50)
t.circle(100,80)
t.setheading(0)
t.forward(40)
t.end_fill()
t.penup()
t.goto(-40,185)
t.color("black","yellow")
t.pendown()
t.begin_fill()


t.forward(70)
t.setheading(90)
t.forward(50)
t.setheading(200)
t.forward(30)
t.setheading(110)
t.forward(45)
t.setheading(250)
t.forward(50)
t.setheading(130)
t.forward(30)
t.setheading(-90)
t.forward(60)
t.goto(-40,185)
t.end_fill()
t.mainloop()
